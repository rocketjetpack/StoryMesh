"""Email delivery module for StoryMesh.

Provides rich HTML email construction and SMTP delivery for generated books.
When a recipient is configured (via ``storymesh.config.yaml`` or a per-run
CLI override), the book assembler node calls :func:`deliver_book` after the
PDF and EPUB have been written to disk.

MIME structure with cover image::

    MIMEMultipart('mixed')
    ├── MIMEMultipart('related')
    │   ├── MIMEMultipart('alternative')
    │   │   ├── MIMEText  (plain-text fallback)
    │   │   └── MIMEText  (HTML body referencing cid:cover_art)
    │   └── MIMEImage     (cover PNG, Content-ID: cover_art)
    ├── MIMEBase          (PDF attachment)
    └── MIMEBase          (EPUB attachment, when include_epub=True)

Without a cover image the ``related`` wrapper is omitted and
``alternative`` sits directly inside ``mixed``.
"""

from __future__ import annotations

import logging
import os
import re
import smtplib
from dataclasses import dataclass
from email import encoders
from email.mime.base import MIMEBase
from email.mime.image import MIMEImage
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText
from pathlib import Path
from typing import Any

logger = logging.getLogger(__name__)

